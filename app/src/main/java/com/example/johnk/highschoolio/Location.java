package com.example.johnk.highschoolio;

public class Location
{
    private double lat, lon;
    private String addy;

    public Location(String add, double la , double lo)
    {
        add = addy;
        la = lat;
        lo = lon;
    }

    public String getAddy()
    {
        return addy;
    }

    public double getLat()
    {
        return lat;
    }

    public double getLong()
    {
        return lon;
    }

}
